def intersection_scan():
    """
    在交叉口执行完整的转向与举手检测序列：

        (面向前方)
            │
    ① 左转 90°   → 面向左侧过道 → 举手检测（阻塞，处理完返回）
    ② 右转 180°  → 面向右侧过道 → 举手检测（阻塞，处理完返回）
    ③ 左转 90°   → 面向前方恢复

    注意：检测程序是阻塞调用，每次转向后等待检测完全结束
          才执行下一次转向。

    角度约定（与 Arduino T 命令一致）:
        正值 = 左转, 负值 = 右转
    """
    # ① 左转 90°，面向左侧，第一次举手检测
    turn(90)
    detect_hands()

    # ② 右转 180°（从左侧转到右侧），第二次举手检测
    turn(-180)
    detect_hands()

    # ③ 左转 90°，恢复面向前进方向
    turn(90)


