# 主控逻辑 — C-SCAN 往复运动
# =====================================================================

def main():
    print("=" * 60)
    print("  小车往复运动控制程序 (C-SCAN 模式)")
    print("=" * 60)
    print(f"  总循环次数: {NUM_CYCLES}")
    print(f"  路线: 交叉口1 → 交叉口2 → 交叉口3 → 交叉口4")
    print(f"        交叉口4 ──(直接退回)──→ 交叉口1")
    print("=" * 60)

    try:
        # ==============================================================
        # 外层循环：往复运动轮次
        # ==============================================================
        for cycle in range(1, NUM_CYCLES + 1):
            print(f"\n{'◆' * 30}")
            print(f"◆ 第 {cycle}/{NUM_CYCLES} 轮往复运动")
            print(f"{'◆' * 30}")

            # ==========================================================
            # 内层循环：按顺序访问4个交叉口
            #   - i=0,1,2 (交叉口1,2,3)：转向检测后 → 循环末尾附加前进指令
            #   - i=3   (交叉口4)    ：转向检测后 → 无前进指令
            # ==========================================================
            for i in range(4):
                intersection_num = i + 1
                suffix = " (终点)" if intersection_num == 4 else ""
                print(f"\n  ┌── 交叉口 {intersection_num}{suffix} ──┐")

                # 执行交叉口转向与举手检测序列
                intersection_scan()

                # 循环末尾附加前进指令（交叉口4除外）
                if i < 3:
                    dist = FORWARD_DISTANCES[i]
                    move(dist)
                # i == 3 (交叉口4): 不前进，内层循环到此结束

            # ==========================================================
            # 循环末尾附加后退指令：从交叉口4直接退回交叉口1
            # 退回过程中不处理新请求
            # ==========================================================
            print(f"\n  ┌── 退回出发点 (交叉口1) ──┐")
            print(f"  (退回过程中不处理举手请求)")
            move(-BACKWARD_DISTANCE)

            print(f"\n  ✓ 第 {cycle}/{NUM_CYCLES} 轮完成，已回到交叉口1")
            print(f"{'◇' * 30}")

        print("\n" + "=" * 60)
        print("  所有往复运动循环完成！程序结束。")
        print("=" * 60)

    finally:
        _close_serial()


# =====================================================================
