# 串口通信配置
# =====================================================================
SERIAL_PORT = '/dev/ttyUSB0'       # Arduino 串口设备（也可能是 /dev/ttyACM0）
BAUD_RATE = 9600
COMMAND_TIMEOUT = 35               # 等待 "Done" 的超时秒数（Arduino 内部限 30s）

import serial
import time as _time

_ser = None

def _open_serial():
    """获取或创建串口连接（懒初始化 + 自动重连）。"""
    global _ser
    if _ser is None or not _ser.is_open:
        _ser = serial.Serial(SERIAL_PORT, BAUD_RATE, timeout=1)
        _time.sleep(2)  # 等待 Arduino 复位完成
    return _ser

def _send_command(cmd_str):
    """
    向 Arduino 发送一行命令，阻塞等待 "Done" 返回。
    期间将 Arduino 的调试输出转发到控制台。

    返回:
        True  — 命令执行成功
        False — 超时
    """
    ser = _open_serial()
    ser.reset_input_buffer()
    ser.write((cmd_str + '\n').encode())

    start = _time.time()
    while _time.time() - start < COMMAND_TIMEOUT:
        line = ser.readline().decode('utf-8', errors='replace').strip()
        if line:
            print(f"    [Arduino] {line}")
        if line == 'Done':
            return True

    print(f"    [错误] 命令 '{cmd_str}' 超时！")
    return False

def _close_serial():
    """关闭串口连接。"""
    global _ser
    if _ser is not None and _ser.is_open:
        _ser.close()
    _ser = None


# =====================================================================
# 底层运动控制接口
# 通过串口 G / T 命令驱动 Arduino 的编码器反馈控制
# =====================================================================

def move(distance_cm):
    """
    直线移动指定距离(cm)，基于编码器反馈控制。
    阻塞调用，直到运动完成。

    参数:
        distance_cm: float
            正值 = 前进
            负值 = 后退
    """
    if distance_cm == 0:
        return

    direction = "前进" if distance_cm > 0 else "后退"
    print(f"    [运动] {direction} {abs(distance_cm):.1f} cm ...")
    _send_command(f"G {distance_cm}")
    print(f"    [运动] {direction}完成 ✓")


def turn(angle_degrees):
    """
    原地转向指定角度，基于编码器反馈控制。
    阻塞调用，直到转向完成。

    参数:
        angle_degrees: float
            正值 = 向左转向
            负值 = 向右转向
    """
    if angle_degrees == 0:
        return

    direction = "左" if angle_degrees > 0 else "右"
    print(f"    [转向] 原地向{direction}旋转 {abs(angle_degrees):.1f}° ...")
    _send_command(f"T {angle_degrees}")
    print(f"    [转向] 转向完成 ✓")


